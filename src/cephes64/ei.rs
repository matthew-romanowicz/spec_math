// $$\mathrm{Ei}(x)=\int_{-\infty}^x\frac{e^t}{t}\,dt$$

pub fn ei(x: f64) -> f64 {
    //! Exponential Integral
    //!
    //! ## DESCRIPTION:
    //!
    //! Evaluates the exponential integral
    #![doc=include_str!("ei.svg")]
    //!
    //! Not defined for `x <= 0`.
    //!
    //! See also [`expn`](crate::cephes64::expn)
    //!
    //! ## ACCURACY:
    //!
    //! Relative error.
    //!
    //!<table>
    //! <tr>
    //!     <th>Arithmetic</th>
    //!     <th>Function</th>
    //!     <th>Domain</th>
    //!     <th># Trials</th>
    //!     <th>Peak</th>
    //!     <th>RMS</th>
    //! </tr>
    //! <tr>
    //!     <td>IEEE</td>
    //!     <td>S(x)</td>
    //!     <td>0, 100</td>
    //!     <td>50000</td>
    //!     <td>8.6e-16</td>
    //!     <td>1.3e-16</td>
    //! </tr>
    //!</table>

    let (f, w): (f64, f64);
    match x {
        x if x <= 0.0 => {
            return std::f64::NAN
        },
        x if x < 2.0 => {
            f = polevl(x,&A,5) / p1evl(x,&B,6);
            return EUL + x.ln() + x * f
        },
        x if x < 4.0 => {
            w = 1.0 / x;
            f = polevl(w,&A6,7) / p1evl(w,&B6,7);
        },
        x if x < 8.0 => {
            w = 1.0 / x;
            f = polevl(w,&A5,7) / p1evl(w,&B5,8);
        },
        x if x < 16.0 => {
            w = 1.0/x;
            f = polevl(w,&A2,9) / p1evl(w,&B2,9);
        },
        x if x < 32.0 => {
            w = 1.0/x;
            f = polevl(w,&A4,7) / p1evl(w,&B4,8);
        },
        x if x < 64.0 => {
            w = 1.0/x;
            f = polevl(w,&A7,5) / p1evl(w,&B7,5);
        },
        x if x.is_infinite() => {
            // Case added by Matthew Romanowicz
            return std::f64::INFINITY
        },
        x => {
            w = 1.0/x;
            f = polevl(w,&A3,8) / p1evl(w,&B3,9);
        }

    }

    x.exp() * w * (1.0 + w * f)
}

#[cfg(test)]
mod ei_tests {
    use super::*;

    #[test]
    fn ei_trivials() {
        assert_eq!(ei(std::f64::NAN).is_nan(), true);
        assert_eq!(ei(0.0).is_nan(), true);
        assert_eq!(ei(-1.0).is_nan(), true);
        assert_eq!(ei(-std::f64::INFINITY).is_nan(), true);
        assert_eq!(ei(std::f64::INFINITY), std::f64::INFINITY);
    }

    #[test]
    fn ei_piecewise() {
        assert_eq!(ei(1e-300), -690.1983122333121);
        assert_eq!(ei(1e-50), -114.55203898480076);
        assert_eq!(ei(1e-20), -45.474486194979384);
        assert_eq!(ei(1.0), 1.895117816355937);
        assert_eq!(ei(2.0-1e-14), 4.954234356001854);

        assert_eq!(ei(2.0), 4.954234356001891);
        assert_eq!(ei(3.0), 9.933832570625416);
        assert_eq!(ei(4.0-1e-14), 19.63087447005608);

        assert_eq!(ei(4.0), 19.63087447005622);
        assert_eq!(ei(6.0), 85.9897621424392);
        assert_eq!(ei(8.0-1e-14), 440.3798995348347);

        assert_eq!(ei(8.0), 440.3798995348382);
        assert_eq!(ei(12.0), 14959.532666397525);
        assert_eq!(ei(16.0-1e-14), 595560.998670831);

        assert_eq!(ei(16.0), 595560.9986708369);
        assert_eq!(ei(24.0), 1154115391.8491828);
        assert_eq!(ei(32.0-1e-14), 2550043566357.761);

        assert_eq!(ei(32.0), 2550043566357.7866);
        assert_eq!(ei(48.0), 1.493630213112993e19);
        assert_eq!(ei(64.0-1e-14), 9.899640925974388e25);

        assert_eq!(ei(64.0), 9.899640925974455e25);
        assert_eq!(ei(300.0), 6.496482508088664e127);

        // In theory the max value that returns non-infinite should be ~716, but 
        // it seems like there's some error in that region.
        assert_eq!(ei(709.75), 2.4548011945788152e305);
        assert_eq!(ei(709.8), std::f64::INFINITY);

    }
}